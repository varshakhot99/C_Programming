
//E to Q Letters Printing using while.

#include<stdio.h>
#include<conio.h>

int main()
{
    char ch = 'E';

    printf("\n\n\n\t*************************************");

    printf("\n\n\n\n\t\tE to Q Characters =>");

    while(ch <= 'Q')
    {
        printf("\n\t %c",ch);
        ch++;
    }

    printf("\n\n\n\t*************************************");

    printf("\n\n\t\tThanks!!!");

   printf("\n\n\n\t*************************************");

    getch();
    return 0;
}

