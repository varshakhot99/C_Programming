
//To printing reverse 1 to 10 numbers using for.

#include<stdio.h>
#include<conio.h>

int main()
{
    int No;

    printf("\n\n\t*********************************************");

    for(No = 10;No >= 1;No--)
    {
        printf("\n\t\t%d",No);
    }

    printf("\n\n\t*********************************************");

    printf("\n\n\t\tThanks!!!");

    printf("\n\n\t*********************************************");

    getch();
    return 0;
}

