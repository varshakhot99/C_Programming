
//Program to accept 10 Bills to display their Summation .

#include<stdio.h>
#include<conio.h>

int main()
{
    int R = 0 , C = 0;

    printf("\n\n\n\t***********************************************************************************************\n");

    for(R = 1 ; R <= 10; R++)
    {
        for(C = 25; C >= 14; C--)
        {
            printf("\t%3d",R * C);
        }
        printf("\n");
    }

    printf("\n\n\t***********************************************************************************************\n");


    printf("\n\n\t\tThanks!!!");

    printf("\n\n\n\n\t***********************************************************************************************\n");

    getch();
    return 0;
}

